    public static String usersToString(){
        String accum = "";
        
        for (int i = 0; i < users.size(); i++) {
            accum += users.get(i).getPlayerName()+ "\n";
        }
        
        return accum;
    }
    
    public static void poblarUsers() throws IOException{
        users = new ArrayList<>();
        
        //Asegura el puntero
        dataBase.seek(0);
        
        while (dataBase.getFilePointer() < dataBase.length()){ //Recorrido de archivo
            dataBase.readUTF(); //Mueve el puntero la cantidad de Bytes que abarque el Nombre Completo del user
            dataBase.skipBytes(12); //Skip 4 (int puntos) + 8 (long fecha) = 12 bytes
            String username = (String)dataBase.readUTF();
            
            HumanSequencePlayer player = new HumanSequencePlayer(users.size() + 1, username);
            
            users.add(player);
            dataBase.readUTF(); //Mueve el puntero la cantidad de Bytes que abarque el password del usuario

        }
    }
    
    /*
    Formato de dataBase.db
    
    String Nombre Completo
    int puntos
    long Fecha de creacion
    String username
    String password
    */
    
    public static void addUser(String nombreCompleto, String username, String password) throws IOException {
        //Setea el archivo en el que vamos a escribir
        dataBase = new RandomAccessFile("dataBase.db", "rw");
        
        dataBase.seek(dataBase.length()); //Setea el puntero al final del archivo
        dataBase.writeUTF(nombreCompleto); //Escribe el nombre completo
        dataBase.writeInt(0); //Escribe la cantidad inicial de puntos
        dataBase.writeLong(Calendar.getInstance().getTimeInMillis()); //Escribe la fecha de creacion
        dataBase.writeUTF(username); //Escribe el username
        dataBase.writeUTF(password); //Escribe el password
    }
    
